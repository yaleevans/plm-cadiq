<h1>Overview</h1>
<p>
	Welcome! This is the CadIQ Job Manager. Use this page to search for CAD files in your favorite PLM
	system, download them to the local CadIQ server, and start an analysis.
</p>

<h1>Instruction</h1>
<ol>
	<li>Go to the "New Job" page and search for a PLM part</li>
	<li>Select the part you want to analyze</li>
	<li>Click the "Download" button to download the part to the local CadIQ server</li>
	<li>Click the "Analyze" button to start an analysis</li>
	<li>Wait for the analysis to complete</li>
	<li>View the results of the analysis</li>
</ol>
